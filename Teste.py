largura = float(input('Digite a largura do campo em metros: '))
comprimento = float(input('Digite o comprimento do campo em metros: '))
area = largura * comprimento
perimetro = (largura * 2) + (comprimento * 2)

print(f"A área do campo é de {area} metros quadrados")
print(f"O perímetro do campo é de {perimetro} metros")
